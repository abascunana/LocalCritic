
package Vista;

public class Cancion {
	private String tituloCancion;
	private String generoCancion;
	private String artistaCancion;
	private String productoraCancion;
	private int duracionCancion;
	private int anoLanzamientoCancion;
	private int mediaCancion;
	
	
	public String getTituloCancion() {
		return tituloCancion;
	}
	public void setTituloCancion(String tituloCancion) {
		this.tituloCancion = tituloCancion;
	}
	public String getGeneroCancion() {
		return generoCancion;
	}
	public void setGeneroCancion(String generoCancion) {
		this.generoCancion = generoCancion;
	}
	public String getArtistaCancion() {
		return artistaCancion;
	}
	public void setArtistaCancion(String artistaCancion) {
		this.artistaCancion = artistaCancion;
	}
	public String getProductoraCancion() {
		return productoraCancion;
	}
	public void setProductoraCancion(String productoraCancion) {
		this.productoraCancion = productoraCancion;
	}
	public int getDuracionCancion() {
		return duracionCancion;
	}
	public void setDuracionCancion(int duracionCancion) {
		this.duracionCancion = duracionCancion;
	}
	public int getAnoLanzamientoCancion() {
		return anoLanzamientoCancion;
	}
	public void setAnoLanzamientoCancion(int anoLanzamientoCancion) {
		this.anoLanzamientoCancion = anoLanzamientoCancion;
	}
	public int getMediaCancion() {
		return mediaCancion;
	}
	public void setMediaCancion(int mediaCancion) {
		this.mediaCancion = mediaCancion;
	}
}