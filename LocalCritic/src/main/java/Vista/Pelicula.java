package Vista;
//Clase con los atributos de la Película
public class Pelicula {
	private String tituloPelicula;
	private String sinopsisPelicula;
	private String generoPelicula;
	private String actor;
	private int anoLanzamientoPelicula;
	private int duracionPelicula;
	private String director;
	private String productoraPelicula;
	private int mediaValPelicula;
	
	public String getTituloPelicula() {
		return tituloPelicula;
	}
	public void setTituloPelicula(String tituloPelicula) {
		this.tituloPelicula = tituloPelicula;
	}
	public String getSinopsisPelicula() {
		return sinopsisPelicula;
	}
	public void setSinopsisPelicula(String sinopsisPelicula) {
		this.sinopsisPelicula = sinopsisPelicula;
	}
	public String getGeneroPelicula() {
		return generoPelicula;
	}
	public void setGeneroPelicula(String generoPelicula) {
		this.generoPelicula = generoPelicula;
	}
	public String getActor() {
		return actor;
	}
	public void setActor(String actor) {
		this.actor = actor;
	}
	public int getAnoLanzamientoPelicula() {
		return anoLanzamientoPelicula;
	}
	public void setAnoLanzamientoPelicula(int anoLanzamientoPelicula) {
		this.anoLanzamientoPelicula = anoLanzamientoPelicula;
	}
	public int getDuracionPelicula() {
		return duracionPelicula;
	}
	public void setDuracionPelicula(int duracionPelicula) {
		this.duracionPelicula = duracionPelicula;
	}
	public String getDirector() {
		return director;
	}
	public void setDirector(String director) {
		this.director = director;
	}
	public String getProductoraPelicula() {
		return productoraPelicula;
	}
	public void setProductoraPelicula(String productoraPelicula) {
		this.productoraPelicula = productoraPelicula;
	}
	public int getMediaValPelicula() {
		return mediaValPelicula;
	}
	public void setMediaValPelicula(int mediaValPelicula) {
		this.mediaValPelicula = mediaValPelicula;
	}
	
	

}
