
package Vista;

public class Libro {
	private String tituloLibro;
	private String sinopsisLibro;
	private String generoLibro;
	private String escritorLibro;
	private String editorialLibro;
	private String ultimaEdicion;
	private int anoLanzamientoLibro;
	private int mediaLibro;
	
	public String getTituloLibro() {
		return tituloLibro;
	}
	public void setTituloLibro(String tituloLibro) {
		this.tituloLibro = tituloLibro;
	}
	public String getSinopsisLibro() {
		return sinopsisLibro;
	}
	public void setSinopsisLibro(String sinopsisLibro) {
		this.sinopsisLibro = sinopsisLibro;
	}
	public String getGeneroLibro() {
		return generoLibro;
	}
	public void setGeneroLibro(String generoLibro) {
		this.generoLibro = generoLibro;
	}
	public String getEscritorLibro() {
		return escritorLibro;
	}
	public void setEscritorLibro(String escritorLibro) {
		this.escritorLibro = escritorLibro;
	}
	public String getEditorialLibro() {
		return editorialLibro;
	}
	public void setEditorialLibro(String editorialLibro) {
		this.editorialLibro = editorialLibro;
	}
	public String getUltimaEdicion() {
		return ultimaEdicion;
	}
	public void setUltimaEdicion(String ultimaEdicion) {
		this.ultimaEdicion = ultimaEdicion;
	}
	public int getAnoLanzamientoLibro() {
		return anoLanzamientoLibro;
	}
	public void setAnoLanzamientoLibro(int anoLanzamientoLibro) {
		this.anoLanzamientoLibro = anoLanzamientoLibro;
	}
	public int getMediaLibro() {
		return mediaLibro;
	}
	public void setMediaLibro(int mediaLibro) {
		this.mediaLibro = mediaLibro;
	}
}