package Vista;

public class Libro {
	
	private int isbn;
	private String tituloLibro;
	private String sinopsisLibro;
	private String generoLibro;	
	private String escritor;
	private String editorial;
	private int anoLanzamientoLibro;
	private int numPag;
	private int ultimaEdicion;
	private int mediaValLibro;


	
	public int getIsbn() {
		return isbn;
	}
	public void setIsbn(int isbn) {
		this.isbn = isbn;
	}
	
	public String getTituloLibro() {
		return tituloLibro;
	}
	public void setTituloLibro(String tituloLibro) {
		this.tituloLibro = tituloLibro;
	}
	public String getSinopsisLibro() {
		return sinopsisLibro;
	}
	public void setSinopsisLibro(String sinopsisLibro) {
		this.sinopsisLibro = sinopsisLibro;
	}
	public String getGeneroLibro() {
		return generoLibro;
	}
	public void setGeneroLibro(String generoLibro) {
		this.generoLibro = generoLibro;
	}
	public String getEscritor() {
		return escritor;
	}
	public void setEscritor(String escritor) {
		this.escritor = escritor;
	}
	public String getEditorial() {
		return editorial;
	}
	public void setEditorial(String editorial) {
		this.editorial = editorial;
	}
	public int getAnoLanzamientoLibro() {
		return anoLanzamientoLibro;
	}
	public void setAnoLanzamientoLibro(int anoLanzamientoLibro) {
		this.anoLanzamientoLibro = anoLanzamientoLibro;
	}
	public int getNumPag() {
		return numPag;
	}
	public void setNumPag(int numPag) {
		this.numPag = numPag;
	}
	public int getUltimaEdicion() {
		return ultimaEdicion;
	}
	public void setUltimaEdicion(int ultimaEdicion) {
		this.ultimaEdicion = ultimaEdicion;
	}
	public int getMediaValLibro() {
		return mediaValLibro;
	}
	public void setMediaValLibro(int mediaValLibro) {
		this.mediaValLibro = mediaValLibro;
	}


}